print(round(1.23333, 2))

# Функция round округляет первое переданное число к количеству символов после запятой переданному вторым числом
# если переданно только одно число то оно округлиться в большую сторону если больше 0.5
# и в меньшую если больше 0.5
# Оба числа и 0.5 и -0.5 округляються к 0
# Можно переопределить в классе с помощю (__round__)