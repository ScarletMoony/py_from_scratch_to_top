print(abs(-123)) # Модуль числа

print(max(1, 2, 3, 4, 5), min(1, 2, 3, 4, 5)) # Максимальное и минимальное

x = [1, 2, 3, 4]

print(max(x)) # Можно использовать со списками

print(pow(2, 8)) # Степеть первого числа во втором

print(round(3.324, 1)) # Округление до определенного количестра символов после ,

print(sum([1, 2, 3])) # Сумма всеж элементов

print(hex(42)) # Перевод символа в разные системы исчесления

print(oct(42))

print(bin(42))

print(all([True, True, True])) # Проверяет все ли True
print(all([True, False, True]))

print(any([False, False, True])) # Проверяет есть ли хоть один True
print(any([False, False, False]))

print(list(zip('abc', (10, 20, 30)))) # Обьединяет два списка в кортеж
